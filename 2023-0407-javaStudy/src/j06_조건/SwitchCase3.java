package j06_조건;

public class SwitchCase3 {

	public static void main(String[] args) {
		int coin = 3;
		
		switch(3) {
		case 1:
			System.out.println("1단계입니다");
			coin--;
			if(coin == 0) {
				break;
			}
		case 2:
			System.out.println("2단계입니다");
			coin--;
			if(coin == 0) {
				break;
			}
		case 3:
			System.out.println("3단계입니다");
			coin--;
			if(coin == 0) {
				break;
			}
		case 4:
			System.out.println("4단계입니다");
			coin--;
			if(coin == 0) {
				break;
			}
		case 5:
			System.out.println("5단계입니다");
			coin--;
			if(coin == 0) {
				break;
			}
		case 6:
			System.out.println("6단계입니다");
			coin--;
			if(coin == 0) {
				break;
			}
	}
		
	}

}
