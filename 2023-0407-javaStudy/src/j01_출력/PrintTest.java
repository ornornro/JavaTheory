package j01_출력;

public class PrintTest {

	public static void main(String[] args) {
		System.out.println("Hello, Java!");
		System.out.print("웹개발 첫 수업입니다.");
		System.out.print("최연호");
		// 한줄 주석 -> //
		/*
		 * 장문 주석 -> /* + enter
		 * 
		 * println은 줄바꿈을 해라는 의미임.
		 */
		System.out.println();
		System.out.println("목요일입니다. \n자바 수업입니다.");
		// \n -> 줄바꿈 표시
		
		
	}

}
